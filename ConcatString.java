import java.util.*;
class ConcatString
{
	public static void main(String args[])
	{
	     Scanner sc = new Scanner(System.in);
	     System.out.println("Enter a First String :");
	     String str1 = sc.next();

	     System.out.println("Enter a Second String :");
	     String str2 = sc.next();

	     System.out.println((str1+str2));


	}
}